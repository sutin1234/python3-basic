# operation & operator
num1 = 3
num2 = 4
print("operation add: %d" % (num1 + num2))
print("operation subtrack: %d" % (num1 - num2))
print("operation multiple: %d" % (num1 * num2))
print("operation devision: %d" % (num1 / num2))
print("operation modulaton: %d" % (num1 % num2))  # mod
print("operation devision and floor: %d" % (num1 // num2))
print("operation power: %d" % (num1 ** num2))

print("num1 > num2: %s" % (num1 > num2))
print("num1 < num2: %s" % (num1 < num2))
print("num1 >= num2: %s" % (num1 >= num2))
print("num1 >= num2: %s" % (num1 <= num2))
print("num1 == num2: %s" % (num1 == num2))

print("num1 is num2: %s" % (num1 is num2))
print("num1 is not num2: %s" % (num1 is not num2))
